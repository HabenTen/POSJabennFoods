    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Get the entered username and password
        String enteredUsername = Username.getText(); 
        String enteredPassword = Password.getText(); 

        // Replace "admin" and "admin" with your predefined username and password
        String correctUsername = "admin";
        String correctPassword = "admin";

        // Check if the entered username and password match the correct ones
        if (enteredUsername.equals(correctUsername) && enteredPassword.equals(correctPassword)) {
            // Authentication successful, show a message or perform further actions
            Inventory inventoryPage = new Inventory();
            inventoryPage.setVisible(true);
            inventoryPage.pack();
            inventoryPage.setLocationRelativeTo(null);
            this.dispose();
        } else {
            // Authentication failed, show an error message
            JOptionPane.showMessageDialog(this, "Invalid username or password. Please try again.", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

